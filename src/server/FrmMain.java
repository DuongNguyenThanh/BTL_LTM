    private void jButton_chooseFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_chooseFileActionPerformed
        // TODO add your handling code here:
        if (isRunningCam) {
            webCam.close();
            isRunningCam = false;
        }

        jTabbedPane.setSelectedIndex(0);

        if (!isRunningVideo) {
            isRunningVideo = true;
            
            JFileChooser fileChooser = new JFileChooser();
            fileChooser.showOpenDialog(jPanel_main);
            File file = fileChooser.getSelectedFile();
            url = file.getPath();
            System.out.println(url);
            jTextArea_displayMessage.setText("check text\n");
            jTextArea_displayMessage.append(url + "\n");
            mediaPlayer.playMedia(url);
        }
    }//GEN-LAST:event_jButton_chooseFileActionPerformed


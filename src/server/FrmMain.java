    private void jButton_cameraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_cameraActionPerformed
        // TODO add your handling code here:
        if (isRunningVideo) {
            mediaPlayer.pause();
            isRunningVideo = false;
        }
        
        jTabbedPane.setSelectedIndex(1);

        if (!isRunningCam) {
            isRunningCam = true;
            webCam.open();
            new VideoFeedTaker().start();
        }
    }//GEN-LAST:event_jButton_cameraActionPerformed

    private void jButton_websiteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_websiteActionPerformed
        // TODO add your handling code here:
        if (isRunningCam) {
            webCam.close();
            isRunningCam = false;
        }
        if (isRunningVideo) {
            mediaPlayer.pause();
            isRunningVideo = false;
        }

        jTabbedPane.setSelectedIndex(2);

        isRuningWeb = 1;

        SwingUtilities.invokeLater(new Runnable() {
            @Override
            public void run() {
                frameVidWeb.add(new JpWebsiteVideo());

                frameVidWeb.setMinimumSize(new Dimension(825, 595));
                frameVidWeb.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                frameVidWeb.setLocationRelativeTo(null);
                frameVidWeb.setVisible(true);
            }
        });

    }//GEN-LAST:event_jButton_websiteActionPerformed

    class VideoFeedTaker extends Thread {

        @Override
        public void run() {
            while (isRunningCam) {
                try {
                    Image image = webCam.getImage();
                    jLabel_displayCamera.setIcon(new ImageIcon(image));
                    Thread.sleep(50);
                } catch (InterruptedException ex) {
                    Logger.getLogger(FrmMain.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }

    }

